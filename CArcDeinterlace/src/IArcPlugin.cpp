#include <IArcPlugin.h>


namespace arc
{
	namespace gen3
	{

		// +----------------------------------------------------------------------------------------------------------+
		// | Constructor                                                                                              |
		// +----------------------------------------------------------------------------------------------------------+
		IArcPlugin::IArcPlugin( void )
		{
			m_pList.reset( new arc::gen3::CArcStringList() );
		}


		// +----------------------------------------------------------------------------------------------------------+
		// | Destructor                                                                                               |
		// +----------------------------------------------------------------------------------------------------------+
		IArcPlugin::~IArcPlugin( void )
		{
		}


		// +----------------------------------------------------------------------------------------------------------+
		// | getNameList                                                                                              |
		// +----------------------------------------------------------------------------------------------------------+
		// | Returns the list of algorithms supported by the plugin.                                                  |
		// |                                                                                                          |
		// | Throws std::runtime_error                                                                                |
		// +----------------------------------------------------------------------------------------------------------+
		arc::gen3::CArcStringList* IArcPlugin::getNameList( void )
		{
			return m_pList.get();
		}


		// +----------------------------------------------------------------------------------------------------------+
		// | getCount                                                                                                 |
		// +----------------------------------------------------------------------------------------------------------+
		// | Returns the number of algorithms supported by the plugin.                                                |
		// |                                                                                                          |
		// | Throws std::runtime_error                                                                                |
		// +----------------------------------------------------------------------------------------------------------+
		std::uint32_t IArcPlugin::getCount( void )
		{
			return m_pList->length();
		}

	}	// end gen3 namespace
}		// end arc namespace

